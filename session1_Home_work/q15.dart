/*Question 15
What does it mean for Dart to be a statically-typed language, 
and how does it impact the way variables are used? */

/* Solution 
Dart is a statically-typed language, which means that the data type of a variable is determined at compile time
 and cannot be changed later.

  *This helps prevent type-related errors, 
  *improves code reliability, 
  *and makes the program easier to understand and maintain.
*/